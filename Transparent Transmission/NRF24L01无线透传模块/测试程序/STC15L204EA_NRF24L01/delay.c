void Delay100ms(void)       //@22.1184MHz
{
	unsigned char i, j, k;
	_nop_();
	_nop_();
	i = 9;
	j = 104;
	k = 139;
	do
	{
		do
		{
			while (--k);
		} while (--j);
	} while (--i);
}

